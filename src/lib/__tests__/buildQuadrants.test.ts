import { EMPTY_CELL as _ } from "@/utils/emptyCell";
import { buildQuadrants } from "../buildQuadrants";

describe("buildQuadrants", () => {
  it("should pass, given a valid input and expecting the valid output", () => {
    expect(
      buildQuadrants(
        [
          [1, _, 3, 4, _, 6, 7, 8, 9],
          [1, 2, _, _, 5, 6, 7, _, 9],
          [1, _, 3, 4, 5, _, 7, 8, 9],

          [_, 2, 3, 4, 5, _, _, 8, 9],
          [1, 2, 3, 4, 5, 6, 7, 8, 9],
          [1, 2, _, _, 5, 6, 7, 8, 9],

          [1, 2, 3, _, 5, 6, 7, 8, 9],
          [1, 2, 3, 4, 5, 6, _, 8, 9],
          [1, 2, _, 4, 5, 6, 7, 8, 9],
        ],
        9,
      ),
    ).toStrictEqual([
      [
        [1, _, 3],
        [1, 2, _],
        [1, _, 3],
      ],
      [
        [4, _, 6],
        [_, 5, 6],
        [4, 5, _],
      ],
      [
        [7, 8, 9],
        [7, _, 9],
        [7, 8, 9],
      ],
      [
        [_, 2, 3],
        [1, 2, 3],
        [1, 2, _],
      ],
      [
        [4, 5, _],
        [4, 5, 6],
        [_, 5, 6],
      ],
      [
        [_, 8, 9],
        [7, 8, 9],
        [7, 8, 9],
      ],
      [
        [1, 2, 3],
        [1, 2, 3],
        [1, 2, _],
      ],
      [
        [_, 5, 6],
        [4, 5, 6],
        [4, 5, 6],
      ],
      [
        [7, 8, 9],
        [_, 8, 9],
        [7, 8, 9],
      ],
    ]);
  });
});
